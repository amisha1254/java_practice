package literals;

public class eg1 {
	public static void main(String[] args) {
		int a = 10;
		System.out.println(a);
		
		double d = 10.00d;
		System.out.println(d);
		
		float f1=10.0f;
		System.out.println(f1);
		
		boolean b1 = true;
		System.out.println(b1);
		
		String s ="amisha";
		System.out.println(s);
		
		int y = 01234;
		System.out.println(y);
		
		int z = 0XEC12;
		System.out.println(z);
	}

}
